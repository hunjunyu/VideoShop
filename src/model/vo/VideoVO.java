package model.vo;

public class VideoVO {
	
	int v_number;		// 비디오번호
	String v_type;		// 장르
	String v_Name;		// 비디오명
	String v_director;	// 감독
	String v_actor;		// 배우
	String v_exp;	    // 설명
	
	//getter,setter
	public int getV_number() {
		return v_number;
	}
	public void setV_number(int v_number) {
		this.v_number = v_number;
	}
	public String getV_type() {
		return v_type;
	}
	public void setV_type(String v_type) {
		this.v_type = v_type;
	}
	public String getV_Name() {
		return v_Name;
	}
	public void setV_Name(String v_Name) {
		this.v_Name = v_Name;
	}
	public String getV_director() {
		return v_director;
	}
	public void setV_director(String v_director) {
		this.v_director = v_director;
	}
	public String getV_actor() {
		return v_actor;
	}
	public void setV_actor(String v_actor) {
		this.v_actor = v_actor;
	}
	public String getV_exp() {
		return v_exp;
	}
	public void setV_exp(String v_exp) {
		this.v_exp = v_exp;
	}
	

	
	

}
